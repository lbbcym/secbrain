"""Insights reporter - generates reports in various formats."""

from __future__ import annotations

import csv
import json
from datetime import datetime
from pathlib import Path

from secbrain.insights.analyzer import AnalysisResults


class InsightsReporter:
    """Generates reports from analysis results."""

    def __init__(self, output_dir: str | Path = "."):
        """
        Initialize reporter.

        Args:
            output_dir: Directory for output files
        """
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)

    def generate_markdown(self, results: AnalysisResults, title: str = "SecBrain Insights Report") -> str:
        """
        Generate Markdown report.

        Args:
            results: Analysis results
            title: Report title

        Returns:
            Markdown formatted report
        """
        lines = []
        lines.append(f"# {title}\n")
        lines.append(f"*Generated: {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M:%S')}*\n")

        # Executive Summary
        lines.append("## Executive Summary\n")
        lines.append(f"**Status:** {results.summary.get('status', 'unknown').replace('_', ' ').title()}\n")
        lines.append(f"**Total Insights:** {results.summary.get('total_insights', 0)}\n")
        lines.append(f"**Critical Issues:** {results.summary.get('critical_count', 0)}\n")
        lines.append(f"**High Priority:** {results.summary.get('high_count', 0)}\n")
        lines.append(f"\n**Next Action:** {results.summary.get('next_action', 'N/A')}\n")

        # Key Metrics
        lines.append("\n## Key Metrics\n")
        for key, value in results.metrics.items():
            formatted_key = key.replace('_', ' ').title()
            if isinstance(value, float):
                lines.append(f"- **{formatted_key}:** {value:.2f}\n")
            else:
                lines.append(f"- **{formatted_key}:** {value}\n")

        # Priority Insights
        critical = results.get_critical_insights()
        if critical:
            lines.append("\n## 🔴 Critical Issues\n")
            for insight in critical:
                lines.append(f"\n### {insight.title}\n")
                lines.append(f"{insight.description}\n")
                lines.append(f"\n**Action Required:** {insight.action}\n")

        high = results.get_high_priority_insights()
        if high:
            lines.append("\n## 🟠 High Priority Items\n")
            for insight in high:
                lines.append(f"\n### {insight.title}\n")
                lines.append(f"{insight.description}\n")
                lines.append(f"\n**Recommended Action:** {insight.action}\n")

        # Insights by Category
        lines.append("\n## Insights by Category\n")
        categories = {i.category for i in results.insights}
        for category in sorted(categories):
            cat_insights = results.get_by_category(category)
            if cat_insights:
                lines.append(f"\n### {category.replace('_', ' ').title()}\n")
                for insight in cat_insights:
                    priority_emoji = {
                        "critical": "🔴",
                        "high": "🟠",
                        "medium": "🟡",
                        "low": "⚪",
                    }.get(insight.priority, "⚪")
                    lines.append(f"- {priority_emoji} **{insight.title}** - {insight.description}\n")

        # Recommendations
        if results.recommendations:
            lines.append("\n## Top Recommendations\n")
            for i, rec in enumerate(results.recommendations, 1):
                lines.append(f"{i}. {rec}\n")

        # Footer
        lines.append("\n---\n")
        lines.append("*Generated by SecBrain Insights*\n")

        return "".join(lines)

