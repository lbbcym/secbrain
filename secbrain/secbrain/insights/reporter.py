    def generate_json(self, results: AnalysisResults) -> str:
        """
        Generate JSON report.

        Args:
            results: Analysis results

        Returns:
            JSON formatted report
        """
        data = {
            "generated_at": datetime.now(timezone.utc).isoformat(),
            "summary": results.summary,
            "metrics": results.metrics,
            "insights": [
                {
                    "category": i.category,
                    "priority": i.priority,
                    "title": i.title,
                    "description": i.description,
                    "action": i.action,
                    "context": i.context,
                    "timestamp": i.timestamp,
                }
                for i in results.insights
            ],
            "recommendations": results.recommendations,
        }
        return json.dumps(data, indent=2)

    def generate_csv(self, results: AnalysisResults) -> str:
        """
        Generate CSV report of insights.

        Args:
            results: Analysis results

        Returns:
            CSV formatted report
        """
        output = StringIO()
        writer = csv.writer(output)

        # Header
        writer.writerow(["Category", "Priority", "Title", "Description", "Action", "Timestamp"])

        # Data
        for insight in results.insights:
            writer.writerow([
                insight.category,
                insight.priority,
                insight.title,
                insight.description,
                insight.action,
                insight.timestamp,
            ])

        return output.getvalue()

    def save_report(
        self,
        results: AnalysisResults,
        format: str = "markdown",
        filename: str | None = None,
    ) -> Path:
        """
        Save report to file.

        Args:
            results: Analysis results
            format: Output format (markdown, html, json, csv)
            filename: Optional filename (auto-generated if not provided)

        Returns:
            Path to saved file
        """
        if filename is None:
            timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
            ext = {"markdown": "md", "html": "html", "json": "json", "csv": "csv"}[format]
            filename = f"insights_report_{timestamp}.{ext}"

        filepath = self.output_dir / filename

        if format == "markdown":
            content = self.generate_markdown(results)
        elif format == "html":
            content = self.generate_html(results)
        elif format == "json":
            content = self.generate_json(results)
        elif format == "csv":
            content = self.generate_csv(results)
        else:
            msg = f"Unsupported format: {format}"
            raise ValueError(msg)

        filepath.write_text(content)
        return filepath

    def save_all_formats(self, results: AnalysisResults, base_filename: str = "insights_report") -> dict[str, Path]:
        """
        Save report in all formats.

        Args:
            results: Analysis results
            base_filename: Base filename (without extension)

        Returns:
            Dictionary mapping format to filepath
        """
        timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
        base = f"{base_filename}_{timestamp}"

        return {
            "markdown": self.save_report(results, "markdown", f"{base}.md"),
            "html": self.save_report(results, "html", f"{base}.html"),
            "json": self.save_report(results, "json", f"{base}.json"),
            "csv": self.save_report(results, "csv", f"{base}.csv"),
        }
